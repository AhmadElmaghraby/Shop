    public void restoreDB(String path) {
        String[] executeCmd = new String[]{"C:/Program Files (x86)/MySQL/MySQL Server 5.0/bin/mysql.exe", "--user=root", "--password=root", "-e", "source " + path};
        Process p = null;
        try {
            p = Runtime.getRuntime().exec(executeCmd);
            int processComplete = p.waitFor();
            if (processComplete == 0) {
                JOptionPane.showMessageDialog(null, "Backup restored successfully");
            } else {
                JOptionPane.showMessageDialog(null, "Could not restore the backup");
            }
        } catch (HeadlessException | IOException | InterruptedException ex) {
            JOptionPane.showMessageDialog(null, "error : " + ex.getMessage());
        }
    }

    public void backupDataWithOutDatabase() {
        try {
            Process p = null;
            Runtime runtime = Runtime.getRuntime();
            p = runtime.exec("C:/Program Files (x86)/MySQL/MySQL Server 5.0/bin/mysqldump.exe -uroot -proot --add-drop-database -B shop -r" + backup_path_txt.getText());
            int processComplete = p.waitFor();
            if (processComplete == 0) {
                JOptionPane.showMessageDialog(null, "Backup created successfully");
            } else {
                JOptionPane.showMessageDialog(null, "Could not create the backup");
            }
        } catch (HeadlessException | IOException | InterruptedException ex) {
            JOptionPane.showMessageDialog(null, "error : " + ex.getMessage());
        }

    }

