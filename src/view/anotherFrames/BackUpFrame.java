    private void pathchooser_restore_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pathchooser_restore_btnActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setFileFilter(new FileNameExtensionFilter("*.sql", "sql"));
        int retval = chooser.showOpenDialog(null);
        if (retval == JFileChooser.APPROVE_OPTION) {
            File file = chooser.getSelectedFile();
            if (file == null) {
                return;
            }
            String filepath = file.getAbsolutePath();
            restore_filepath_txt.setText(filepath);
        }
    }//GEN-LAST:event_pathchooser_restore_btnActionPerformed

