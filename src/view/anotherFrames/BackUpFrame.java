    private void pathchooser_backup_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pathchooser_backup_btnActionPerformed
        JFileChooser chooser = new JFileChooser();
        int retval = chooser.showSaveDialog(null);
        if (retval == JFileChooser.APPROVE_OPTION) {
            File file = chooser.getSelectedFile();
            if (file == null) {
                return;
            }
         
            String date = new SimpleDateFormat("yyyy-MM-dd").format(new Date());
            String filepath = file.getAbsolutePath();
            filepath = filepath + "_" + date + ".sql";
            backup_path_txt.setText(filepath);
        }
    }//GEN-LAST:event_pathchooser_backup_btnActionPerformed

